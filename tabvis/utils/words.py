def random_int(max_value: int) -> int:
    """Return a cryptographically random integer in ``[0, max_value)``.

    Parity with the TS ``randomInt``: read a big-endian ``UInt32`` from 4 CSPRNG bytes and
    reduce modulo ``max_value`` (so the modulo bias for non-power-of-two list lengths matches
    the source exactly).
    """
    value = int.from_bytes(secrets.token_bytes(4), "big")
    return value % max_value


def pick_random(array: Sequence[str]) -> str:
    """Pick a random element from ``array``."""
    return array[random_int(len(array))]


def generate_word_slug() -> str:
    """Generate a random ``"adjective-verb-noun"`` slug.

    Example: ``"gleaming-brewing-phoenix"``, ``"cosmic-pondering-lighthouse"``.
    """
    adjective = pick_random(ADJECTIVES)
    verb = pick_random(VERBS)
    noun = pick_random(NOUNS)
    return f"{adjective}-{verb}-{noun}"


def generate_short_word_slug() -> str:
    """Generate a shorter random ``"adjective-noun"`` slug.

    Example: ``"graceful-unicorn"``, ``"cosmic-lighthouse"``.
    """
    adjective = pick_random(ADJECTIVES)
    noun = pick_random(NOUNS)
    return f"{adjective}-{noun}"
